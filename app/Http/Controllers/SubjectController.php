    public function register($id)
    {
        $check = User::where('_id',auth()->user()->_id)->where('subjects.subject_id', $id)->get();
        if (sizeof($check) != null) {
            User::where('_id',auth()->user()->_id)->pull('subjects',['subject_id'=>$id]);
        }else
            User::where('_id',auth()->user()->_id)->push('subjects', ['subject_id' => $id, 'ts' => time()]);
    }

